/*===== SENDING AN EMAIL VIA CONTACT SECTION =====*/
const express = require("express");
const nodemailer = require("nodemailer");
const bodyParser = require("body-parser");
const cors = require("cors");

const app = express();
const PORT = 3000;

// Middleware
app.use(cors());
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: true }));

// POST route to handle form submission
app.post("/send-email", async (req, res) => {
  const { name, email, message } = req.body;

  // Configure the email transporter
  const transporter = nodemailer.createTransport({
    service: "gmail", // Use your email provider, e.g., Gmail
    auth: {
      user: "sinthunakorn.suntisuk01@gmail.com", // Your email address
      pass: "Root_1234!", // Your email password (use an App Password for Gmail)
    },
  });

  // Email details
  const mailOptions = {
    from: email,
    to: "sinthunakorn.suntisuk01@gmail.com", // Replace with your email address
    subject: `Contact Form Submission from ${name}`,
    text: `You have received a new message:\n\nName: ${name}\nEmail: ${email}\nMessage: ${message}`,
  };

  // Send the email
  try {
    await transporter.sendMail(mailOptions);
    res.status(200).json({ message: "Email sent successfully!" });
  } catch (error) {
    console.error("Error sending email:", error);
    res.status(500).json({ message: "Error sending email", error });
  }
});

// Start the server
app.listen(PORT, () => {
  console.log(`Server is running on http://localhost:${PORT}`);
});
